import * as ActionType from "../actionTypes/AllActionTypes";

export const AddAccessToKen = (data) => {
  return {
    type: ActionType.ADD_ACCESS_TOKEN,
    data: {
      access_token: data,
      refresh_token: "00",
    },
  };
};

